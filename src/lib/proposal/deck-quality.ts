/**
 * A lightweight per-slide visual tracker. Template renderers call `mark()` after
 * they emit a chart / table / callout / timeline / card grid; the exporter calls
 * `assert()` once the slide is fully composed.
 */
export class VisualGuard {
  private present = false;

  mark(): void {
    this.present = true;
  }

  hasVisual(): boolean {
    return this.present;
  }

  assert(title: string): void {
    if (!this.present) {
      throw new Error("Slide has no visual element: " + title);
    }
  }
}
